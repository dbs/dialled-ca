<h3>Library</h3>

<p>The purpose of the Tudor and Cashel Public Library is to provide all individuals in the community with carefully selected books and other materials to aid the individual in the pursuit of education, information, research, pleasure, and the creative use of leisure time.</p>

<p>Because of the volume of publishing, as well as the limitations of budget and space, the library must have a selection policy with which to meet community interests and needs.</p>

<p>Some of the services our library offers are:</p>

<ul>
	<li>Building program and/or building maintenance</li>
	<li>Recreational print materials</li>
	<li>Recreational video and audio materials</li>
	<li>Print, video and electronic information with the skilled reference staff to use it</li>
	<li>Resource sharing</li>
	<li>Community information</li>
</ul>

