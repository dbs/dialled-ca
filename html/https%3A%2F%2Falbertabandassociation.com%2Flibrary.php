    <h1>MUSIC LENDING LIBRARY</h1>
    <p><strong> R. Bruce Marsh Memorial Library Lending Regulations </strong></p>
    <p>The lending library is available for use: </p>
    <ul>
      <li><strong>In person<br />
      </strong>Education building | University of Alberta [<a href="pdf/map_UofA.pdf" target="_blank" class="pdf">F5 on map</a>] </li>
      <li><strong>By telephone</strong> <br />
        (403) 347.2237 <br />
        Toll-free  1.877.687.4239 </li>
      <li><strong><a href="member/library_itemslist.php">Online</a></strong>
        <ul>
          <li>All users are allowed to search the online database.</li>
          <li>In order to rent materials you must be an <a href="membership_privileges.php">ABA member</a>.</li>
        </ul>
      </li>
    </ul>
    <p>Included in the library are predominantly concert band titles. Some stage band scores, band method, recordings and other teaching aids are also available. </p>
    <p>A movable track storage system assists in making access simple, allows for expandability and  reduces damage to the scores.</p>
    <p> Questions? Please feel free to contact us by phone at (403) 347.2237, or toll-free at 1.877.687.4239. </p>
    <p><strong>Library Fees </strong><br />
      The library service is available to all members of the Alberta Band Association. Regular, Community Band, Institution, Organization, and Student pay a nominal user fee, while honorary members receive complimentary service. </p>
    <ul>
      <li>A fee of <strong>$10</strong> will be charged for each score borrowed.
        <ul>
          <li>A <strong>maximum of 7 scores</strong> can be signed out for a rental period of 4 months. </li>
        </ul>
      </li>
      <li>Scores can be <strong>renewed </strong>for a <strong>$10</strong> fee, assuming that a hold has not been placed on that item. </li>
      <li>Overdue materials will be assessed a fine on a per-week basis. Overdue printed materials will be charged $2/week. </li>
      <li>All handling and postage will be paid for by the member and included on the invoice. </li>
      <li>Damaged and lost parts will be replaced at cost to the borrower.    </li>
    </ul>
