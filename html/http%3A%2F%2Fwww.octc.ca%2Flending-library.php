			<a name="4"></a>
			<h2>How to Borrow from the Online Lending Library</h2> 

			<p>OCTC clients and their families who are registered with the Lending Library can login to reserve items online by clicking on the green 'login' button near the top right-hand corner of the <a href="http://www.lendinglibrary.octc.ca/4dcgi/gen_2002/Lang=Def" target="_blank">Lending Library's homepage</a>.</p>
			<p>Next, you will be prompted to login with your last/family name and password.  If you do not yet have a password, please contact us at 613-831-5127.</p>
			<p>You can browse through the Lending Library's holdings for items that interest you by searching the inventory by a variety of criteria, such as keyword, author, title or subject.</p>
			<p>If you find something you'd like to borrow, you can reserve an item that is currently available by clicking on it to 'add it to your bookbag'.  You will be contacted to arrange a convenient pick-up time and location.</p>
			<p>If you reserve an item that is currently out on loan, you will be put on a waiting list to borrow the item and we will contact you when it becomes available.</p>
			<p>If you need assistance while searching through the Online Lending Library, you can select 'Help' from the top right-hand corner of the Library's homepage.</p>  
			<p><strong>For other questions about the Lending Library, please call Brigitte Loughlin at 613-737-0871, ext. 2943 or <a href="mailto:bloughlin@octc.ca">bloughlin@octc.ca</a></strong></p> 

