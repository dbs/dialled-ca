            <h1>Family and children's services</h1>

            <p class="pageintro">Promotes, strengthens and sustains effective parenting and positive family functioning, and ensures that children are protected from abuse and neglect, and receive the care essential for their well being.</p>
            
                        

            

            <!-- BEGIN SECTION -->
<h2>Child care services</h2>



<p>Ensures quality, accessible and comprehensive childcare services for Yukon children and families. Some services include: childcare subsidies; direct operating grants to licensed child facilities. Callers should be directed to the number listed for detailed programming descriptions.</p>
<p><A href="http://www.hss.gov.yk.ca/childcareunit.php" class="more">more information</A></P>

<!-- END SECTION --><!-- BEGIN SECTION -->
<h2>Child placement services</h2>



<p>Promotes, coordinates and provides assessment and placements for children and counseling treatment and support to children and their caregivers in order to ensure the healthy development of children and to enhance the functioning of the caregiver family. Services offered are adoption services, child abuse treatment services, services to children in care, foster care services, custody, and access. </p>
<p><A href="http://www.hss.gov.yk.ca/childplacement.php" class="more">more information</A></P>

<!-- END SECTION --><!-- BEGIN SECTION -->
<h2>Child protection</h2>



<p>Provides child protection services: investigation and assessment of suspected child abuse/neglect; prevention and support services; counseling and/or treatment services. Callers should be directed to the number listed for detailed programming descriptions. </p>
<p><A href="http://www.hss.gov.yk.ca/childprotection.php" class="more">more information</A></P>

<!-- END SECTION --><!-- BEGIN SECTION -->
<h2>Early childhood</h2>



<p>The Yukon's Early Childhood Branch provides a range of supports for children, and their families to ensure they get the best possible start in life, as well as through their early school years, through a variety of programs. </p>
<p><A href="http://www.hss.gov.yk.ca/earlychildhood.php" class="more">more information</A></P>

<!-- END SECTION --><!-- BEGIN SECTION -->
<h2>Family supports for children with disabilities</h2>



<p>Family supports for children with disabilities provides support to Yukon families to care for their child with a disability (up to 19 years of age). </p>
<p><A href="http://www.hss.gov.yk.ca/disabilities_children.php" class="more">more information</A></P>

<!-- END SECTION --><!-- BEGIN SECTION -->
<h2>Report child abuse</h2>



<p>All Yukoners have a responsibility to ensure the protection of children from abuse.</p>
<p><A href="http://www.hss.gov.yk.ca/childabuse.php" class="more">more information</A></P>

<!-- END SECTION --><!-- BEGIN SECTION -->
<h2>Youth justice</h2>



<p>Works to prevent or reduce the incidence of youth crime; promote health and well-being of youth and families; deliver safe, secure and culturally sensitive programs in response to youth crime, and promote healthy re-integration of young persons into the community. Services offered include Youth Probation, Custody Services and Community Programs, Youth Achievement Centre, and Youth High Risk Treatment Program.</p>
<p><A href="http://www.hss.gov.yk.ca/youthjustice.php" class="more">more information</A></P>

<!-- END SECTION -->

            
