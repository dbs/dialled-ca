<h1 class="teacher-header">Library</h1>
			<p>
	We have a huge library consisting of two floors with more than 14,000 fiction and non-fiction books for students to choose from. All classes from grade JK to 8 have library classes every week. Grade 9 students come in on their own time.</p>
<p>
	Scholastic Reading Counts is a big hit with most students in grades 3 to 6. They read books, do short computer generated quizzes, earn points and win prizes. At each prize level their names go into a year-end draw for some bigger prizes.</p>
<p>
	There is always much excitement at the two Scholastic Book Fairs we have each year; one in the fall and the other in the spring. This is a great opportunity for students to build their own little library of favorites at home. The proceeds from these fairs go toward buying more resources for the library.</p>
<p>
